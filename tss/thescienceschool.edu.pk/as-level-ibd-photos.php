	<script type="text/javascript">
		//alert('a1');
		jQuery(document).ready(function($) {
			//alert('a2');
			// hide messages 
			$("#error").hide();
			$("#show_message").hide();
			// on submit...
			$('#registrationForm').submit(function(e) {
				//alert('a3');
				e.preventDefault();
				$("#error").hide();
				//name required
				var name = $("input#name").val();
				if (name == "") {
					$("#error").fadeIn().text("Name required.");
					$("input#name").focus();
					return false;
				}
				// email required
				var email = $("input#email").val();
				if (email == "") {
					$("#error").fadeIn().text("Email required");
					$("input#email").focus();
					return false;
				}
				// email required
				var campus = $("input#email").val();
				if (campus == "") {
					$("#error").fadeIn().text("Campus required");
					$("input#campus").focus();
					return false;
				}
				// email required
				var clas = $("input#email").val();
				if (clas == "") {
					$("#error").fadeIn().text("Class required");
					$("input#class").focus();
					return false;
				}
				// phone number required
				var phone = $("input#phone").val();
				if (phone == "") {
					$("#error").fadeIn().text("Mobile number required");
					$("input#phone").focus();
					return false;
				}
				// password number required
				var password = $("input#password").val();
				if (password == "") {
					$("#error").fadeIn().text("Password number required");
					$("input#password").focus();
					return false;
				}
				// password number required
				var re_password = $("input#re-password").val();
				if (re_password == "") {
					$("#error").fadeIn().text("Re-Password number required");
					$("input#re-password").focus();
					return false;
				}
				if (re_password != password) {
					$("#error").fadeIn().text("Password and re-password should be same");
					$("input#re-password").focus();
					return false;
				}
				// ajax
				$.ajax({
					type: "POST",
					url: "https://emis.thescienceschool.edu.pk/RegistrationAPI/api/UserRegistrationApi.php",
					data: $(this).serialize(), // get all form field value in serialize form
					success: function() {
						$("#show_message").fadeIn();
						//$("#ajax-form").fadeOut();
					}
				});
			});
			return false;
		});
