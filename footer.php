<script type="text/javascript">
    jQuery(document).ready(function($) {
        // hide messages 
        $("#error").hide();
        $("#show_message").hide();
        // on submit...
        $("#registrationForm").submit(function(e) {
            e.preventDefault();
            $("#error").hide();

            //name required
            var name = $("input#name").val();
            if (name == "") {
                $("#error").fadeIn().text("Name required.");
                $("input#name").focus();
                return false;
            }
            // email required
            var email = $("input#email").val();
            if (email == "") {
                $("#error").fadeIn().text("Email required");
                $("input#email").focus();
                return false;
            }
            // campus required
            var campus = $("select#campus").val();
            if (campus == "") {
                $("#error").fadeIn().text("Campus required");
                $("input#campus").focus();
                return false;
            }
            // class required
            var clas = $("select#class").val();
            if (clas == "") {
                $("#error").fadeIn().text("Class required");
                $("input#class").focus();
                return false;
            }
            // phone number required
            var phone = $("input#phone").val();
            if (phone == "") {
                $("#error").fadeIn().text("Mobile number required");
                $("input#phone").focus();
                return false;
            }
            // password required
            var password = $("input#password").val();
            if (password == "") {
                $("#error").fadeIn().text("Password required");
                $("input#password").focus();
                return false;
            }
            // re-password required
            var re_password = $("input#re-password").val();
            if (re_password == "") {
                $("#error").fadeIn().text("Re-Password required");
                $("input#re-password").focus();
                return false;
            }
            if (re_password != password) {
                $("#error")
                    .fadeIn()
                    .text("Password and re-password should be same");
                $("input#re-password").focus();
                return false;
            }
            // ajax
            $.ajax({
                type: "POST",
                url: "http://192.168.100.54/tss-emis-10/workplace/RegistrationAPI/api/UserRegistrationApi.php",
                data: {
                    name: name,
                    email: email,
                    campus: campus,
                    class: clas,
                    phone: phone,
                    password: password
                },

                success: function(resp) {
                    if (resp == "Error Occur") {
                        $("#error").fadeIn().text('Error 404 occured');
                        $("#error").fadeOut(5000);
                    } else if (resp == "Success") {
                        $("#show_message").fadeIn();
                        $("#show_message").fadeOut(5000);
                    } else if (resp == "Email Error") {
                        $("#error").fadeIn().text('Email Already Exists');
                        $("#error").fadeOut(5000);
                    } else if (resp == "Field missing") {
                        $("#error").fadeIn().text('Required Field is missing');
                        $("#error").fadeOut(5000);
                    } else if (resp == "Server connection") {
                        $("#error").fadeIn().text('Problem connecting to server');
                        $("#error").fadeOut(5000);
                    } else {
                        $("#error").fadeIn();
                        $("#error").fadeOut(5000);
                    }

                },
            });
        });
        return false;
    });
</script>

